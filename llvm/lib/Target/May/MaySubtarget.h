#ifndef LLVM_LIB_TARGET_MAY_MAYSUBTARGET_H
#define LLVM_LIB_TARGET_MAY_MAYSUBTARGET_H

#include "May.h"
#include "MayFrameLowering.h"
#include "MayISelLowering.h"
#include "MayInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "MayGenSubtargetInfo.inc"

namespace llvm {

class StringRef;
class TargetMachine;



class MaySubtarget : public MayGenSubtargetInfo {
  virtual void anchor();

  MayRegisterInfo RegInfo;
  MayInstrInfo InstrInfo;
  MayFrameLowering FrameLowering;
  MayTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;
public:
  MaySubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
                const TargetMachine &TM);
  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const MayInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const MayFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const MayTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const MayRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_MAY_MAYSUBTARGET_H